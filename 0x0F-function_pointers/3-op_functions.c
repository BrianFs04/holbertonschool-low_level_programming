#include "3-calc.h"

/**
 * op_add - adds
 * @a: first integer
 * @b: second integer
 * Return: The addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction
 * @a: first integer
 * @b: second integer
 * Return: The substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: first integer
 * @b: second integer
 * Return: The multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first integer
 * @b: second integer
 * Return: The division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: first integer
 * @b: second integer
 * Return: The modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
