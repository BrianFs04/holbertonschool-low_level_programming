/**
 * add - function that sums 2 numbers
 * @a: first value
 * @b: second value
 * Return: The addition
 */
int add(a, b)
{
	return (a + b);
}
/**
 * sub - function that substracts 2 numbers
 * @a: first value
 * @b: second value
 * Return: The substraction
 */
int sub(a, b)
{
	return (a - b);
}
/**
 * mul - function that multiplies 2 numbers
 * @a: first value
 * @b: second value
 * Return: The multiplication
 */
int mul(a, b)
{
	return (a * b);
}
/**
 * div - function that divides 2 numbers
 * @a: first value
 * @b: second value
 * Return: The division
 */
int div(a, b)
{
	return (a / b);
}
/**
 * mod - function that modules 2 numbers
 * @a: first value
 * @b: second value
 * Return: The module of 2 numbers
 */
int mod(a, b)
{
	return (a % b);
}
